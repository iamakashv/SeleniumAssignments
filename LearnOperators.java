package week1.day1;

public class LearnOperators {

	public static void main(String[] args) {
	
		int a = 20;
		int b = 10;
		
		//Arithmetic operators
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		//Comparison
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a==b);
		
		//rELATIONAL 
		
		System.out.println(a>b && b>a );
		System.out.println(a>b || b>a );
		
	
	
	
	}
	

}
